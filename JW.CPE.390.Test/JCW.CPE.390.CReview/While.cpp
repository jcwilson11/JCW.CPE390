#include <iostream>
using namespace std;

int main () {

    int index = 1; //the variable must initialied with a value before starting a loop
    while(index <= 5) { //in the parentheses () you need to input the condition that keeps the loop running
        cout << index << endl;
        index++; //adding ++ to the end of the variable increases it by one

    }


    return 0;
}